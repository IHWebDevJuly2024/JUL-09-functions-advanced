// REVIEW ABOUT EQUAL SIGNS (this is an interview question)
let age = 29;

age = 30; // THIS IS CHANGING THE VALUE. Age now is 30
age == 30; // NOW WE ARE COMPARING VALUES. The age is still 30 and we are going to get TRUE
age === 30; // WE ARE COMPARING VALUES AND DATA TYPES. BOTH OF THEM ARE numbers, so the output is TRUE.

